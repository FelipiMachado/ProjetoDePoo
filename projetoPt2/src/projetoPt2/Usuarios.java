package projetoPt2;

/**
 * Usuarios
 */
public class Usuarios {
    public String nome;
    public String email;
    public String senha;
    public String descricao;

    public Usuarios(String nome, String email, String senha, String descricao) {
        this.nome = nome;
        this.email = email;
        this.senha = senha; 
        this.descricao = descricao;
    }
}
